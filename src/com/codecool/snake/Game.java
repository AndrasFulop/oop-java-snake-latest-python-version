package com.codecool.snake;

import com.codecool.snake.entities.enemies.SimpleEnemy;
import com.codecool.snake.entities.powerups.SimplePowerup;
import com.codecool.snake.entities.snakes.SnakeHead;
import com.codecool.snake.Main;
import javafx.application.Platform;
import javafx.scene.Scene;
import javafx.scene.control.*;
import javafx.scene.control.Menu;
import javafx.scene.control.MenuBar;
import javafx.scene.control.MenuItem;
import javafx.scene.layout.Border;
import javafx.scene.layout.BorderPane;
import javafx.scene.layout.Pane;
import javafx.scene.text.Font;
import javafx.scene.text.Text;
import javafx.stage.Stage;

import java.awt.*;

import static com.codecool.snake.Globals.WINDOW_WIDTH;

public class Game extends Pane {

    public Game() {
        new SnakeHead(this, 500, 500);

        new SimpleEnemy(this);
        new SimpleEnemy(this);
        new SimpleEnemy(this);
        new SimpleEnemy(this);

        new SimplePowerup(this);
        new SimplePowerup(this);
        new SimplePowerup(this);
        new SimplePowerup(this);

        BorderPane root = new BorderPane();

        MenuBar menuBar = new MenuBar();
        menuBar.setPrefWidth(WINDOW_WIDTH);

        root.setTop(menuBar);

        // File menu
        Menu fileMenu = new Menu("File");

        // File menu item
        MenuItem newGameMenuItem = new MenuItem("New Game");
        MenuItem exitMenuItem = new MenuItem("Exit game");
        exitMenuItem.setOnAction(actionEvent -> Platform.exit());

        // Adding menu items to the File Menu
        fileMenu.getItems().addAll(newGameMenuItem, new SeparatorMenuItem(), exitMenuItem);

        // Adding File Menu to Menu Bar
        menuBar.getMenus().addAll(fileMenu);
    }

    public void start() {
        Scene scene = getScene();
        scene.setOnKeyPressed(event -> {
            switch (event.getCode()) {
                case LEFT:  Globals.leftKeyDown  = true; break;
                case RIGHT: Globals.rightKeyDown  = true; break;
            }
        });

        scene.setOnKeyReleased(event -> {
            switch (event.getCode()) {
                case LEFT:  Globals.leftKeyDown  = false; break;
                case RIGHT: Globals.rightKeyDown  = false; break;
            }
        });
        Globals.gameLoop = new GameLoop();
        Globals.gameLoop.start();
    }
}
